CREATE TABLE CLIENTES (
    Cod_Cliente INTEGER PRIMARY KEY,
    Sobrenome VARCHAR (50),
    Nome VARCHAR (50),
    Inicial CHAR (1),
    DDD VARCHAR (5),
    Telefone VARCHAR (15),
    Saldo DECIMAL
);

CREATE TABLE FATURAS (
    Num_Fatura INTEGER PRIMARY KEY,
    Data_Fatura DATE,
    FK_CLIENTES_Cod_Cliente INTEGER
);

CREATE TABLE LINHAS (
    Num_Linha INTEGER PRIMARY KEY,
    Unidade INTEGER,
    Valor DECIMAL,
    FK_FATURAS_Num_Fatura INTEGER,
    FK_PRODUTOS_Cod_Produto INTEGER
);

CREATE TABLE PRODUTOS (
    Cod_Produto INTEGER PRIMARY KEY,
    Descricao VARCHAR (100),
    Data_Compra DATE,
    Quantidade INTEGER,
    Valor DECIMAL,
    Desconto DECIMAL,
    FK_FORNECEDORES_Cod_Fornecedor INTEGER
);

CREATE TABLE FORNECEDORES (
    Cod_Fornecedor INTEGER PRIMARY KEY,
    Nome VARCHAR (60),
    Contato VARCHAR (20),
    DDD CHAR (5),
    Telefone VARCHAR (15),
    Estado VARCHAR (20),
    Cidade VARCHAR (50)
);
 
